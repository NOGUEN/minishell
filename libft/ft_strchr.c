#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	a;

	a = -1;
	while (++a < ft_strlen(s) + 1)
	{
		if (*(s + a) == (char)c)
			return ((char*)(s + a));
	}
	return (NULL);
}
